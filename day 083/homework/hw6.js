// მოცემულია რიცხვების მასივი, რომელიც შეიძლება შეიცავდეს როგორც დადებით, ისე უარყოფით მნიშვნელობებს.
// reduce-ით გამოითვალე მხოლოდ დადებითი რიცხვების ნამრავი.
// თუ დადებითი რიცხვი არცერთი იყოს, დააბრუნე 1.




let arr = [-10,-2,-7,-9,-5,-3,-1]

let redfunc = (acc, el) => {
    if(el > 0){
        return acc * el
    }else{
        return acc 
    }
}

let res = arr.reduce(redfunc,1)

console.log(res)